#!/bin/bash
# ============================================================================
# Sparse Training Speedup Benchmark — Llama-3.1-8B-Instruct — Single H200 GPU
#
# Runs 4 training methods with identical hyperparams and collects timing JSON:
#   1. Dense + AdamW
#   2. Dense + SGD
#   3. LoRA + AdamW
#   4. Sparse BSR + SparseAdamW
#
# Submit: sbatch scripts/benchmark_speedup_llama8b.sh
# ============================================================================
#SBATCH --job-name=bench_8b
#SBATCH --output=logs/bench_8b_%j.out
#SBATCH --error=logs/bench_8b_%j.err
#SBATCH --partition=gpu
#SBATCH --nodes=1
#SBATCH --ntasks=1
#SBATCH --gres=gpu:h200:1
#SBATCH --mem=128G
#SBATCH --time=24:00:00

set -euo pipefail

# ── Repo root ───────────────────────────────────────────────────────────────
if [ -n "${SLURM_SUBMIT_DIR:-}" ] && [ -d "${SLURM_SUBMIT_DIR}" ]; then
    REPO_ROOT="$(cd "${SLURM_SUBMIT_DIR}" && pwd)"
else
    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
    REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
fi
cd "$REPO_ROOT"
mkdir -p logs

echo "====== Llama-3.1-8B Speedup Benchmark ======"
echo "Date:     $(date)"
echo "Node:     $(hostname)"
echo "Job ID:   ${SLURM_JOB_ID:-local}"
echo "GPU:      ${CUDA_VISIBLE_DEVICES:-auto}"

# ── Environment ─────────────────────────────────────────────────────────────
source "$HOME/miniconda/etc/profile.d/conda.sh" 2>/dev/null || source "$(conda info --base)/etc/profile.d/conda.sh" 2>/dev/null
conda activate /home/$USER/.conda/envs/rl_casino || conda activate /scratch/$USER/conda_envs/rl_casino

export PYTHONPATH="$(pwd):$PYTHONPATH"
export PYTHONPATH="/scratch/$USER/pip_packages:$PYTHONPATH"
export PYTORCH_CUDA_ALLOC_CONF=expandable_segments:True

# Storage — all on /scratch
export HF_HOME="/scratch/$USER/hf_cache"
export HF_DATASETS_CACHE="/scratch/$USER/hf_cache/datasets"
export TRITON_CACHE_DIR="/scratch/$USER/triton_cache"
mkdir -p "$HF_HOME" "$HF_DATASETS_CACHE" "$TRITON_CACHE_DIR"

# Copy HF token if needed (for gated Llama model)
if [ -f "$HOME/.cache/huggingface/token" ] && [ ! -f "$HF_HOME/token" ]; then
    cp "$HOME/.cache/huggingface/token" "$HF_HOME/token"
fi

echo "Env:      $(which python)"
echo "HF_HOME:  $HF_HOME"

# ── Shared Config ───────────────────────────────────────────────────────────
MODEL="meta-llama/Llama-3.1-8B-Instruct"
DATASET="math-220k"
N_STEPS=50
SUBSET=512
BATCH_SIZE=1
GRAD_ACCUM=8
NUM_GENERATIONS=8
GEN_BATCH_SIZE=8
LR=1e-6

# Mask directories — generated by run_llama8b_all_masks_and_probes.sh
COLD_MASK_DIR="/scratch/$USER/rl_casino_masks/llama8b_cold"
WARM_GRPO_MASK_DIR="/scratch/$USER/rl_casino_masks/llama8b_warm_grpo"

TIMESTAMP=$(date +%Y%m%d_%H%M%S)
OUTPUT_DIR="/scratch/$USER/rl_casino_outputs/benchmark_llama8b_${TIMESTAMP}"
CACHE_DIR="$HF_DATASETS_CACHE"
mkdir -p "$OUTPUT_DIR"

PASS=0
FAIL=0
TOTAL=0

run_and_track() {
    local NAME="$1"; shift
    echo ""
    echo "============================================================"
    echo "[$((TOTAL+1))] $NAME"
    echo "============================================================"
    TOTAL=$((TOTAL + 1))
    if "$@"; then
        echo "  -> $NAME: PASSED"
        PASS=$((PASS + 1))
    else
        echo "  -> $NAME: FAILED"
        FAIL=$((FAIL + 1))
    fi
}

# ── 1. Dense + AdamW ────────────────────────────────────────────────────────
run_and_track "Dense + AdamW" \
    python src/full_training/GRPO_timing_baseline.py \
        --model_name "$MODEL" \
        --dataset "$DATASET" \
        --n_steps "$N_STEPS" \
        --subset_size "$SUBSET" \
        --batch_size "$BATCH_SIZE" \
        --grad_accum "$GRAD_ACCUM" \
        --num_generations "$NUM_GENERATIONS" \
        --generation_batch_size "$GEN_BATCH_SIZE" \
        --lr "$LR" \
        --optimizer adamw \
        --use_wandb \
        --output_base_dir "$OUTPUT_DIR" \
        --dataset_cache_dir "$CACHE_DIR"

# ── 2. Dense + SGD ──────────────────────────────────────────────────────────
run_and_track "Dense + SGD" \
    python src/full_training/GRPO_timing_baseline.py \
        --model_name "$MODEL" \
        --dataset "$DATASET" \
        --n_steps "$N_STEPS" \
        --subset_size "$SUBSET" \
        --batch_size "$BATCH_SIZE" \
        --grad_accum "$GRAD_ACCUM" \
        --num_generations "$NUM_GENERATIONS" \
        --generation_batch_size "$GEN_BATCH_SIZE" \
        --lr "$LR" \
        --optimizer sgd \
        --use_wandb \
        --output_base_dir "$OUTPUT_DIR" \
        --dataset_cache_dir "$CACHE_DIR"

# ── 3. LoRA + AdamW ─────────────────────────────────────────────────────────
run_and_track "LoRA + AdamW (r=16)" \
    python src/full_training/lora_grpo_timing.py \
        --model_name "$MODEL" \
        --dataset "$DATASET" \
        --n_steps "$N_STEPS" \
        --subset_size "$SUBSET" \
        --batch_size "$BATCH_SIZE" \
        --grad_accum "$GRAD_ACCUM" \
        --num_generations "$NUM_GENERATIONS" \
        --generation_batch_size "$GEN_BATCH_SIZE" \
        --lr "$LR" \
        --optimizer adamw \
        --lora_rank 16 \
        --use_wandb \
        --output_base_dir "$OUTPUT_DIR" \
        --dataset_cache_dir "$CACHE_DIR"

# ── 4. Sparse BSR + SparseAdamW — all available masks ──────────────────────
# Iterate over cold masks (snip, cav, fisher × dpo, grpo) and warm GRPO masks
# (magnitude, momentum, fisher). Skip any that don't exist.

MASK_COUNT=0
for MASK_FILE in \
    "$COLD_MASK_DIR"/cold_snip_dpo.pt \
    "$COLD_MASK_DIR"/cold_snip_grpo.pt \
    "$COLD_MASK_DIR"/cold_cav_dpo.pt \
    "$COLD_MASK_DIR"/cold_cav_grpo.pt \
    "$COLD_MASK_DIR"/cold_fisher_dpo.pt \
    "$COLD_MASK_DIR"/cold_fisher_grpo.pt \
    "$WARM_GRPO_MASK_DIR"/warm_magnitude_grpo.pt \
    "$WARM_GRPO_MASK_DIR"/warm_momentum_grpo.pt \
    "$WARM_GRPO_MASK_DIR"/warm_fisher_grpo.pt \
; do
    if [ ! -f "$MASK_FILE" ]; then
        echo ""
        echo "SKIPPING Sparse BSR — mask not found: $MASK_FILE"
        continue
    fi
    MASK_NAME=$(basename "$MASK_FILE" .pt)
    MASK_COUNT=$((MASK_COUNT + 1))
    run_and_track "Sparse BSR ($MASK_NAME)" \
        python src/full_training/sparse_grpo_bsr.py \
            --model_name "$MODEL" \
            --dataset "$DATASET" \
            --n_steps "$N_STEPS" \
            --subset_size "$SUBSET" \
            --batch_size "$BATCH_SIZE" \
            --grad_accum "$GRAD_ACCUM" \
            --num_generations "$NUM_GENERATIONS" \
            --generation_batch_size "$GEN_BATCH_SIZE" \
            --lr "$LR" \
            --optimizer sparse_adamw \
            --mask "$MASK_FILE" \
            --block_size_bsr 16 \
            --block_size_adam 128 \
            --use_wandb \
            --output_base_dir "$OUTPUT_DIR" \
            --dataset_cache_dir "$CACHE_DIR" \
            --save_model false
done

if [ "$MASK_COUNT" -eq 0 ]; then
    echo ""
    echo "============================================================"
    echo "WARNING: No mask files found. Checked:"
    echo "  $COLD_MASK_DIR/"
    echo "  $WARM_GRPO_MASK_DIR/"
    echo "============================================================"
fi

# ── Collect Results ──────────────────────────────────────────────────────────
echo ""
echo "============================================================"
echo "Collecting timing results..."
echo "============================================================"

python scripts/generate_report.py "$OUTPUT_DIR"

echo ""
echo "============================================================"
echo "BENCHMARK COMPLETE"
echo "============================================================"
echo "Passed: $PASS / $TOTAL"
echo "Failed: $FAIL / $TOTAL"
echo "Output: $OUTPUT_DIR"
echo "Finished: $(date)"

[ "$FAIL" -gt 0 ] && exit 1 || exit 0
